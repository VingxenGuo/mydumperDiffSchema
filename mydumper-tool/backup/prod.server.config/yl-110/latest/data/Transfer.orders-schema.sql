CREATE TABLE `orders` (
  `id` varchar(100) NOT NULL COMMENT '訂單號',
  `agent` varchar(20) NOT NULL COMMENT '会员所属YL代理',
  `account` varchar(190) NOT NULL COMMENT 'YL会员帐号',
  `userId` varchar(100) NOT NULL COMMENT 'FB用戶ID',
  `merchantId` varchar(20) NOT NULL COMMENT '渠道ID',
  `merchantUserId` varchar(100) NOT NULL COMMENT '渠道用戶ID',
  `currency` smallint(6) NOT NULL COMMENT '幣種ID see enum: https://doc.newsportspro.com/single_wallet.html#currency',
  `exchangeRate` varchar(100) DEFAULT NULL COMMENT '匯率快照',
  `seriesType` tinyint(1) NOT NULL COMMENT '關次類型，0 單關、1 串關',
  `betType` varchar(20) NOT NULL COMMENT '投注類型',
  `allUp` tinyint(4) NOT NULL COMMENT '總關數',
  `allUpAlive` tinyint(4) DEFAULT NULL COMMENT '存活關數',
  `stakeAmount` varchar(20) NOT NULL COMMENT '投注額(本金)',
  `settleAmount` varchar(20) DEFAULT NULL COMMENT '結算派獎金額',
  `orderStatus` tinyint(1) NOT NULL COMMENT '訂單狀態, see enum: https://doc.newsportspro.com/single_wallet.html#order_status',
  `payStatus` tinyint(1) DEFAULT NULL COMMENT '付款狀態, see enum: https://doc.newsportspro.com/single_wallet.html#pay_status',
  `oddsChange` tinyint(1) DEFAULT NULL COMMENT '是否接受賠率變更設置：0不接受，1 接受更好賠率，2接受任意賠率',
  `device` varchar(10) DEFAULT NULL COMMENT '設備類型（pc、h5、mobile)',
  `ip` varchar(128) DEFAULT NULL COMMENT '投注IP地址',
  `settleTime` varchar(20) DEFAULT NULL COMMENT '訂單結算時間',
  `createTime` varchar(20) NOT NULL COMMENT '訂單創建時間',
  `modifyTime` varchar(20) NOT NULL COMMENT '訂單確認時間',
  `cancelTime` varchar(20) DEFAULT NULL COMMENT '訂單取消時間',
  `thirdRemark` varchar(200) DEFAULT NULL COMMENT '第三方備註',
  `maxWinAmount` varchar(20) DEFAULT NULL COMMENT '最大可贏金額',
  `loseAmount` varchar(20) DEFAULT NULL COMMENT '最大賠付金額',
  `rollBackCount` tinyint(4) DEFAULT NULL COMMENT '回滾次數',
  `itemCount` tinyint(4) NOT NULL COMMENT '選項數',
  `seriesValue` tinyint(4) NOT NULL COMMENT '串幾關',
  `betNum` smallint(6) NOT NULL,
  `cashOutTotalStake` varchar(20) DEFAULT NULL COMMENT '提前結算總本金',
  `cashOutPayoutStake` varchar(20) DEFAULT NULL COMMENT '提前結算總派獎額',
  `reserveId` varchar(100) DEFAULT NULL COMMENT '預約訂單單號',
  `cashOutCount` tinyint(4) DEFAULT NULL COMMENT '提前結算次數',
  `unitStake` varchar(20) DEFAULT NULL COMMENT '每單金額(混合串關時用到)',
  `reserveVersion` tinyint(4) DEFAULT NULL COMMENT '預約訂單版本號',
  `maxStake` varchar(20) DEFAULT NULL COMMENT '最大投注額',
  `validSettleStakeAmount` varchar(20) DEFAULT NULL COMMENT '有效已結算投注額',
  `validSettleAmount` varchar(20) DEFAULT NULL COMMENT '有效返還額',
  `cashOutCancelStake` varchar(20) DEFAULT NULL COMMENT '提前結算取消總額',
  `walletType` tinyint(1) DEFAULT NULL COMMENT '錢包類型, see enum: https://doc.newsportspro.com/single_wallet.html#wallet_type',
  `version` tinyint(4) NOT NULL COMMENT '數據變更標記，升序，根據大小可判斷是否是最新的數據',
  `rowCreateTime` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '資料创建时间',
  `profitReportDate` varchar(20) DEFAULT NULL COMMENT '紀錄營利報表日期',
  PRIMARY KEY (`id`),
  KEY `agent` (`agent`),
  KEY `account` (`account`),
  KEY `id` (`id`),
  KEY `index_agent_rowCreateTime` (`agent`,`rowCreateTime`) USING BTREE,
  KEY `index_account_rowCreateTime` (`account`,`rowCreateTime`),
  KEY `index_merchantUserId_rowCreateTime` (`merchantUserId`,`rowCreateTime`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;
