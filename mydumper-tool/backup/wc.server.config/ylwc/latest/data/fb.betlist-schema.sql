CREATE TABLE `betlist` (
  `id` varchar(100) NOT NULL COMMENT 'ID',
  `orderId` varchar(100) NOT NULL COMMENT '訂單ID',
  `agent` int(11) NOT NULL COMMENT '会员所属YL代理',
  `account` varchar(190) NOT NULL COMMENT 'YL会员帐号',
  `sportId` smallint(6) NOT NULL COMMENT '運動ID , see enum: https://doc.newsportspro.com/single_wallet.html#sports',
  `matchId` varchar(20) NOT NULL COMMENT '比賽ID',
  `matchName` varchar(100) DEFAULT NULL COMMENT '比賽名稱',
  `period` int(11) NOT NULL COMMENT '階段ID , see enum: https://doc.newsportspro.com/single_wallet.html#period',
  `marketId` varchar(20) NOT NULL COMMENT '玩法ID',
  `marketType` int(11) NOT NULL COMMENT '玩法類型 , see enum: https://doc.newsportspro.com/single_wallet.html#market_type',
  `optionType` smallint(6) NOT NULL COMMENT '投注項類型 , see enum: https://doc.newsportspro.com/single_wallet.html#selection_type',
  `optionName` varchar(100) DEFAULT NULL COMMENT '選項名稱',
  `marketName` varchar(100) DEFAULT NULL COMMENT '玩法名稱',
  `tournamentId` varchar(20) NOT NULL COMMENT '聯賽ID',
  `tournamentName` varchar(100) DEFAULT NULL COMMENT '聯賽名稱',
  `odds` varchar(20) NOT NULL COMMENT '歐式賠率',
  `oddsFormat` tinyint(1) NOT NULL COMMENT '投注時賠率類型, see enum: https://doc.newsportspro.com/single_wallet.html#odds_format_type_enum',
  `betOdds` varchar(20) NOT NULL COMMENT '投注時賠率',
  `settleStatus` tinyint(1) DEFAULT NULL COMMENT '結算狀態, see enum: https://doc.newsportspro.com/single_wallet.html#settle_status',
  `settleResult` tinyint(1) DEFAULT NULL COMMENT '結算結果, see enum: https://doc.newsportspro.com/single_wallet.html#outcome',
  `isInplay` tinyint(1) DEFAULT NULL COMMENT '是否滾球(false [0]:非滾球 true[1]:滾球)',
  `remark` varchar(200) DEFAULT NULL COMMENT '備註',
  `p1` int(11) DEFAULT NULL COMMENT '变量1 (例如:让几个球)',
  `p2` int(11) DEFAULT NULL COMMENT '变量2 (例如:让几个球)',
  `p3` int(11) DEFAULT NULL COMMENT '变量3 (例如:让几个球)',
  `extendedParameter` varchar(50) DEFAULT NULL COMMENT '亚洲让球线',
  `extraInfo` varchar(20) DEFAULT NULL COMMENT '当前比分',
  `pendingTime` varchar(20) DEFAULT NULL COMMENT '延迟等待时间',
  `betScore` varchar(20) DEFAULT NULL COMMENT '注当时比分',
  `cancelReason` tinyint(4) DEFAULT NULL COMMENT '取消原因 , see enum: https://doc.newsportspro.com/single_wallet.html#order_cancel_reason',
  `cancelReasonName` varchar(200) DEFAULT NULL COMMENT '取消原因文本',
  `matchType` tinyint(4) DEFAULT NULL COMMENT '赛事类型, see enum: https://doc.newsportspro.com/single_wallet.html#match_type',
  `matchTime` varchar(20) DEFAULT NULL COMMENT '开赛时间',
  `version` tinyint(4) NOT NULL COMMENT '數據變更標記，升序，根據大小可判斷是否是最新的數據',
  `rowCreateTime` timestamp NULL DEFAULT CURRENT_TIMESTAMP COMMENT '資料创建时间',
  PRIMARY KEY (`id`),
  KEY `agent` (`agent`),
  KEY `account` (`account`),
  KEY `orderId` (`orderId`,`version`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;
